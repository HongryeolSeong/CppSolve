#include <iostream>
using namespace std;

int main()
{
    int N = 5;      // 도달해야할 칸 수
    int ans = 0;    // 건전지 사용량

    // 점프는 이동거리 만큼 건전지 사용량이 증가하고
    // 순간이동은 건전지 사용량에 영향을 주지 않으므로
    // 순간이동을 최대한 사용할 것

    // N칸을 순간이동을 최대한 사용하여 이동했다는 가정하에
    // 2로 나눠주며
    // 홀수가 나오는 경우는 1칸 점프했다고 가정하여
    // 건전지 사용량을 증가시켜준다
    while (N != 0)
    {
        if (N % 2 != 0)
        {
            ans++;
        }
        N /= 2;
    }

    cout << ans;

    return 0;
}